
--  HOW TO TELL IF IT IS A PATIENT OR NOT BY 

IF OBJECT_ID('TEMPDB..#PATlist') IS NOT NULL DROP TABLE #PATlist;
SELECT DISTINCT ID.PAT_ID
       , ID.IDENTITY_ID
	     , ID.LINE
	     , ID.IDENTITY_TYPE_ID
	     , ITYPE.ID_TYPE  --will tell if it's OUR SYSTEMpatient 
	      , ITYPE.ID_TYPE_NAME
INTO #PATlist
FROM IDENTITY_ID AS ID
     LEFT JOIN IDENTITY_ID_TYPE AS ITYPE ON ITYPE.ID_TYPE = ID.IDENTITY_TYPE_ID
WHERE IDENTITY_ID IN ('000000000','0000000','000000000')
